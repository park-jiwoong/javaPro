public class Ex01_Fork {

	public static void main(String[] args) {
		/* [Git-Fork 설명]
		"깃 포크(Git Fork)"는 깃(Git)에서 프로젝트를 복사하여 독립적인 작업 공간을 만들 때 사용되는 개념입니다. 깃 포크는 주로 오픈 소스 프로젝트에서 다른 사람들이 원본 프로젝트를 수정하거나 개선할 수 있도록 허용하는 방법으로 사용됩니다.

		포크는 원본 저장소(repository)를 그대로 복사한 후, 자신의 계정 아래에서 독립적으로 작업할 수 있는 기능입니다. 원본 저장소에 대한 권한이 없어도, 포크된 저장소에서 자유롭게 수정하고, 그 후 변경 사항을 원본 프로젝트에 반영할 수 있는 방식인 "풀 리퀘스트(Pull Request)"를 통해 기여할 수 있습니다.

		포크의 주요 용도:
		기여: 오픈 소스 프로젝트에 기여할 때, 포크를 이용하여 자신의 변경 사항을 원본 프로젝트에 반영할 수 있도록 합니다.
		독립적 작업: 원본 프로젝트와 별개로 자유롭게 실험하거나, 새로운 기능을 추가할 수 있습니다.
		버전 관리: 자신의 저장소에서 실험을 진행하면서 원본 프로젝트에 영향을 주지 않도록 할 수 있습니다.
		포크 과정:
		원본 프로젝트의 저장소에서 "Fork" 버튼을 클릭하여 해당 저장소를 자신의 GitHub 계정에 복사합니다.
		복사된 저장소에서 필요한 작업을 수행합니다.
		작업이 완료되면, 원본 저장소로 변경 사항을 반영할 수 있도록 "Pull Request"를 생성합니다.
		원본 프로젝트의 관리자가 이를 검토하고, 반영 여부를 결정합니다.
		즉, 깃 포크는 원본 프로젝트를 수정하지 않고도 자유롭게 작업할 수 있는 방법을 제공하며, 협업이나 실험을 위한 유용한 기능입니다.
		*/

		/* [Pull Request 설명]
		"Pull Request" (PR)은 Git에서 사용되는 협업 도구로, 다른 사람이 작업한 변경 사항을 원본 프로젝트에 반영하도록 요청하는 과정입니다. 주로 GitHub와 같은 온라인 Git 저장소 관리 플랫폼에서 사용됩니다.

		Pull Request는 작업을 완료한 후, 자신의 저장소에서 변경 사항을 원본 저장소로 가져와 병합(Merge)할 수 있도록 요청하는 기능입니다. 이를 통해 다른 사람들과 협업할 때 변경 사항을 효율적으로 검토하고 논의하며 통합할 수 있습니다.

		Pull Request의 주요 과정:
		포크 및 브랜치 생성:

		오픈 소스 프로젝트나 다른 사람의 프로젝트에 기여하고자 할 때, 해당 저장소를 "포크"하거나 "브랜치"를 만들어 작업을 시작합니다.
		변경 사항 커밋:

		로컬 저장소나 포크된 저장소에서 파일을 수정하고, 그 변경 사항을 커밋(commit)하여 저장합니다.
		Pull Request 생성:

		변경 사항이 완료되면, GitHub와 같은 플랫폼에서 **Pull Request(PR)**를 생성하여 원본 저장소에 병합을 요청합니다.
		이 때, 어떤 변경 사항을 적용했는지에 대한 설명을 작성할 수 있습니다.
		검토 및 논의:

		PR을 생성한 후, 원본 프로젝트의 관리자(maintainer)나 팀원들이 이 변경 사항을 검토합니다.
		코드에 문제가 없는지, 기존 코드와 충돌이 없는지 등을 확인하고, 필요한 경우 논의할 수 있습니다.
		병합(Merge):

		PR이 승인되면, 원본 프로젝트에 변경 사항이 병합됩니다.
		변경 사항이 제대로 반영되었는지 확인한 후, PR이 완료됩니다.
		Pull Request의 주요 용도:
		협업: 여러 사람이 동시에 작업하는 프로젝트에서 변경 사항을 쉽게 검토하고 병합할 수 있게 도와줍니다.
		코드 리뷰: PR을 통해 다른 사람들이 작성한 코드를 리뷰하고 피드백을 제공할 수 있습니다.
		버전 관리: 변경 사항을 원본 프로젝트에 반영하기 전에 꼼꼼히 검토하고, 충돌을 방지하며, 안전하게 통합할 수 있습니다.
		예시:
		내가 어떤 오픈 소스 프로젝트에 기여하고 싶다면, 해당 프로젝트를 포크하고, 내 저장소에서 기능을 추가하거나 버그를 수정합니다.
		작업이 끝나면, GitHub에서 원본 프로젝트에 "Pull Request"를 생성하여 내가 작업한 내용을 원본 저장소에 반영해 달라고 요청합니다.
		프로젝트 관리자는 이를 확인하고, 문제가 없다면 내 변경 사항을 원본 저장소에 병합합니다.
		Pull Request는 협업을 원활하게 하고, 코드 품질을 유지하는 중요한 기능입니다.
		*/

	} // main

} // class
  