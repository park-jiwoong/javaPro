package days02;

public class Ex03_02 {
	
	public static void main(String[] args) {
		
		// 덧셈연산자 +       숫자
		System.out.println( 3 + 5 );  // 8
		//                 문자열+숫자
		// 문자열연결연산자
		System.out.println("3" + 5);  // "35"
		System.out.println("3" + 5.14);  // "35.14"
		System.out.println("3" + true);  // "3true"		
		System.out.println("3"+"5");  // "35"
		
		
	} // main

}
