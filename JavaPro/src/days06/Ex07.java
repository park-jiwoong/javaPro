package days06;

public class Ex07 {
	public static void main(String[] args) {
	// [★시험 숙제★] 순서도 1~100 홀수의 합
	// for 순서도
	// while 순서도
		
		// ASCII + for 문 출력.
		// 256가지의 문자
		// 0~31 제어문자	'\n' '\t' 제어문자들이 들어가 있다
		// 32 ~ 127 일반문자 (대65 ~ 소97 ~ 숫48~)
		//128 ~
		
		for (int i = 0; i < 256; i++) {
		System.out.printf("[%d('%c')]\n", i, (char)i);
			
		
		}// for i
		
	}// main
}// class
